/*********************************************************************
 * @fn      ADC1_IRQHandler
 *
 * @brief   ADC1_2 Interrupt Service Function.
 *
 * @return  none
 */
void SysTick_Handler(void) {

    uint32_t pon_count;
    uint16_t volume;

    //   GPIO_WriteBit(GPIOD, GPIO_Pin_3, Bit_SET);

    volume = 0;

    systick_count++;

    for (int i = 0; i < POLY; i++) {
        pon_count = ponsttat_tick[i]++;
        if (pon_count < pon_tick[i]) {
            ;
        } else if (pon_count < poff_tick[i]) {
            volume++;
        } else {
            //  volume++;
            ponsttat_tick[i] = 0;
        }
    }

    TIM1->CH1CVR = volume * (256 / POLY);
    //   TIM1->CH1CVR = systick_count % 256;

    //  GPIO_WriteBit(GPIOD, GPIO_Pin_3, Bit_RESET);

    SysTick->SR &= 0;
}
