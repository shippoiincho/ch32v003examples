static inline void poly_set(uint8_t ch, uint8_t note) {

    uint32_t interval;

    if (note != 0) {

        interval = 1000000 / tonedata[note];
        interval /= SAMPLING_FREQ;

        pon_tick[ch] = interval / 2;
        poff_tick[ch] = interval;

    } else {

        pon_tick[ch] = UINT32_MAX;
        poff_tick[ch] = UINT32_MAX;

    }

}

/*********************************************************************
 * @fn      main
 *
 * @brief   Main program.
 *
 * @return  none
 */
int main(void) {

    uint32_t score_tick;
    uint32_t sstick_count[POLY];
    uint8_t next_tone, next_length;
    uint8_t ssfin[POLY], total_fin;
    int8_t sstrans[POLY];
    uint32_t last_tick;
    uint32_t songcount = 0;

    //   USART_Printf_Init(115200);
    //   printf("SystemClk:%d\r\n", SystemCoreClock);

    NVIC_EnableIRQ(SysTicK_IRQn);
    SysTick->SR &= ~(1 << 0);
    //   SysTick->CMP = SystemCoreClock/1000-1;
    SysTick->CMP = SystemCoreClock / (SAMPLING_FREQ * 1000) - 1;
    SysTick->CNT = 0;
    SysTick->CTLR = 0xF;

    while(systick_count<48000*5);
    // fail safe

    toneinit();
    TIM1_PWMOut_Init(256, 0, 255);  // 48MHz * 256 = 5us

    while(1) {

        switch (songcount%6) {
            case 0:
            score_tick= (60*1000000/score0_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score0[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score0_trans[i];
            }

            break;

            case 1:
            score_tick= (60*1000000/score1_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score1[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score1_trans[i];
            }

            break;

            case 2:
            score_tick= (60*1000000/score2_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score2[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score2_trans[i];
            }

            break;

            case 3:
            score_tick= (60*1000000/score3_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score3[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score3_trans[i];
            }

            break;

            case 4:
            score_tick= (60*1000000/score4_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score4[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score4_trans[i];
            }

            break;

            case 5:
            score_tick= (60*1000000/score5_tempo)/48; // in usec
            score_tick/=(1000000/(SAMPLING_FREQ*1000));

            total_fin=0;

            // init

            for(int i=0;i<POLY;i++) {
                score_ptr[i]=score5[i];
                sstick_count[i]=0;
                ssfin[i]=0;
                sstrans[i]=score5_trans[i];
            }

            break;

        }

        songcount++;

        // tick start

        while(total_fin<POLY) {

            last_tick=systick_count;

            for(int i=0;i<POLY;i++) {
                if(ssfin[i]==0) {
                    if(sstick_count[i]==0) {
                        next_tone=*score_ptr[i]++;

                        if(next_tone==255) {
                            // end of score
                            ssfin[i]=1;
                            total_fin=0;
                            for(int j=0;j<POLY;j++) {
                                if(ssfin[j]==1) total_fin++;
                            }
                        }
                        next_length=*score_ptr[i]++;

                        // change tone

                        //                 printf("%d,%d,%d\n",i,next_tone,next_length);

                        poly_set(i,next_tone+sstrans[i]);

                        sstick_count[i]=next_length-1;

                    } else {
                        if(sstick_count[i]==1) {
                            poly_set(i,0);
                        }
                        sstick_count[i]--;
                    }
                }
            }

            // wait for next tick

            while(systick_count<(last_tick+score_tick));
        }

        // wait for next song

        last_tick=systick_count;
        while(systick_count<(last_tick+SAMPLING_FREQ*5000));

    }

}

